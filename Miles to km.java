import java.util.Scanner;


public class Assignment1
	{
	static Scanner sc=new Scanner(System.in);
	static final double CONVERT_MILES=1.609;
	public static void main(String[]args)
		{System.out.print("Enter number of miles: ");
		double numMile=sc.nextDouble();
		double numMileKilo=numMile*CONVERT_MILES;
		System.out.printf("%.3f miles convert to %.3f kilometers", numMile, numMileKilo);
		
		}
	}
